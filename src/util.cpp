std::string string_to_hex(const std::string& input)
{
    static const char* const lut = "0123456789ABCDEF";
    size_t len = input.length();

    std::string output;
    output.reserve(2 * len);
    for (size_t i = 0; i < len; ++i)
    {
        const unsigned char c = input[i];
        output.push_back(lut[c >> 4]);
        output.push_back(lut[c & 15]);
    }
    return output;
}

std::string hex_to_string(const std::string& input)
{
    static const char* const lut = "0123456789ABCDEF";
    size_t len = input.length();
    if (len & 1) throw std::invalid_argument("odd length");

    std::string output;
    output.reserve(len / 2);
    for (size_t i = 0; i < len; i += 2)
    {
        char a = input[i];
        const char* p = std::lower_bound(lut, lut + 16, a);
        if (*p != a) throw std::invalid_argument("not a hex digit");

        char b = input[i + 1];
        const char* q = std::lower_bound(lut, lut + 16, b);
        if (*q != b) throw std::invalid_argument("not a hex digit");

        output.push_back(((p - lut) << 4) | (q - lut));
    }
    return output;
}

std::vector<std::string> split(const std::string& s, const std::string& delimiter) {
    std::vector<std::string> list;
    size_t pos = 0;
    std::string token;
    std::string t(s);
    while ((pos = t.find(delimiter)) != std::string::npos) {
        token = t.substr(0, pos);
        list.push_back(token);
        t.erase(0, pos + delimiter.length());
    }

    list.push_back(t.substr(0,pos));
    return list;
}

std::tuple<std::string, std::string, std::string> parse_point(const std::string& s) {
    std::vector<std::string> points = split(s, std::string("\n"));

    std::vector<std::string> xs = split(points.at(0), std::string(" "));
    std::string x = std::accumulate(xs.begin(), xs.end(), std::string(""));

    std::vector<std::string> ys = split(points.at(1), std::string(" "));
    std::string y = std::accumulate(ys.begin(), ys.end(), std::string(""));

    std::vector<std::string> zs = split(points.at(2), std::string(" "));
    std::string z = std::accumulate(zs.begin(), zs.end(), std::string(""));

    return std::make_tuple(x,y,z);
}

